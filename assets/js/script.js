function setText(selector, value) {
  const el = document.querySelector(selector);
  if (el && typeof value === "string") {
    el.textContent = value;
  }
}

function setAllText(selector, value) {
  if (typeof value !== "string") return;
  document.querySelectorAll(selector).forEach((el) => {
    el.textContent = value;
  });
}

function setLang(lang) {
  const dict = translations[lang] || translations.en;

  localStorage.setItem("shambalaLang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  document.querySelectorAll("[data-event-full]").forEach((btn) => {
    const key = btn.dataset.eventFull;
    if (dict[key]) {
      btn.dataset.fullText = dict[key];
    }
  });

  document.querySelectorAll("[data-event-title]").forEach((btn) => {
    const key = btn.dataset.eventTitle;
    if (dict[key]) {
      btn.dataset.title = dict[key];
    }
  });

  document.title = document.querySelector("[data-page-title]")
    ? dict[document.querySelector("[data-page-title]").dataset.pageTitle] || document.title
    : document.title;
}

document.addEventListener("click", (e) => {
  const langBtn = e.target.closest("[data-lang]");
  if (langBtn) {
    e.preventDefault();
    setLang(langBtn.dataset.lang);
  }

  const menuToggle = e.target.closest(".menu-toggle");
  if (menuToggle) {
    const nav = document.querySelector(".main-nav");
    if (nav) nav.classList.toggle("open");
  }

  const navLink = e.target.closest(".main-nav a");
  if (navLink) {
    const nav = document.querySelector(".main-nav");
    if (nav) nav.classList.remove("open");
  }

  const chatOpen = e.target.closest("[data-chat-open]");
  if (chatOpen) {
    const chat = document.querySelector(".chat-widget");
    if (chat) chat.classList.add("open");
  }

  const chatClose = e.target.closest("[data-chat-close]");
  if (chatClose) {
    const chat = document.querySelector(".chat-widget");
    if (chat) chat.classList.remove("open");
  }

  const learnBtn = e.target.closest("[data-event-full]");
  if (learnBtn) {
    const modal = document.querySelector(".modal");
    const title = document.querySelector("[data-modal-title]");
    const text = document.querySelector("[data-modal-text]");

    if (modal && title && text) {
      title.textContent = learnBtn.dataset.title || "";
      text.textContent = learnBtn.dataset.fullText || "";
      modal.classList.add("open");
      document.body.style.overflow = "hidden";
    }
  }

  if (e.target.matches(".modal") || e.target.closest("[data-modal-close]")) {
    const modal = document.querySelector(".modal");
    if (modal) {
      modal.classList.remove("open");
      document.body.style.overflow = "";
    }
  }
});

document.addEventListener("keydown", (e) => {
  if (e.key === "Escape") {
    const modal = document.querySelector(".modal");
    const chat = document.querySelector(".chat-widget");
    const nav = document.querySelector(".main-nav");

    if (modal && modal.classList.contains("open")) {
      modal.classList.remove("open");
      document.body.style.overflow = "";
    }

    if (chat && chat.classList.contains("open")) {
      chat.classList.remove("open");
    }

    if (nav && nav.classList.contains("open")) {
      nav.classList.remove("open");
    }
  }
});

document.addEventListener("DOMContentLoaded", () => {
  setLang(localStorage.getItem("shambalaLang") || "en");
});